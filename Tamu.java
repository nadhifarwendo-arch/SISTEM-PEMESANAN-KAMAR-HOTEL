public class Tamu {
    private String nama;
    private int durasiMenginap;

    public Tamu(String nama, int durasiMenginap) {
        this.nama = nama;
        this.durasiMenginap = durasiMenginap;
    }

    // Getter dan Setter
    public String getNama() {
        return nama;
    }

    public int getDurasiMenginap() {
        return durasiMenginap;
    }
}
